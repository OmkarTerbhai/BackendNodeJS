/* 
    Operators in Javascript
*/

var a = 10;
var b = 2;

console.log("a + b = ", a+b);
console.log("a - b = ", a-b);
console.log("a * b = ", a*b);
console.log("a / b = ", a/b);
console.log("a % b = ", a%b);

//Short Hand assignment operators
a += 2;
console.log(a);
a -= 4;
console.log(a);
a *= 4;
console.log(a);
a /= 2;
console.log(a);
a %= 3;
console.log(a);
a = 2;
a **= 2;
console.log(a);
